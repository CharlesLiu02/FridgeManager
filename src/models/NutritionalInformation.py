class NutritionalInformation():
    def __init__(self):
        self.total_servings = 0
        self.calories_per_serving = 0
        self.calories_from_fat = 0
        self.total_fat = 0
        self.saturated_fat = 0
        self.cholesterol = 0
        self.sodium = 0
        self.carbs = 0
        self.dietary_fiber = 0
        self.sugars = 0
        self.proteins = 0
    
    def get_total_servings(self):
        return self.total_servings
    
    def get_calories_per_servings(self):
        return self.calories_per_serving
    
    def get_calories_from_fat(self):
        return self.calories_from_fat
    
    def get_total_fat(self):
        return self.total_fat
    
    def get_saturated_fat(self):
        return self.saturated_fat
    
    def get_cholesterol(self):
        return self.cholesterol
    
    def get_sodium(self):
        return self.sodium
    
    def get_carbs(self):
        return self.carbs
    
    def get_dietary_fiber(self):
        return self.dietary_fiber
    
    def get_sugars(self):
        return self.sugars
    
    def get_proteins(self):
        return self.proteins
    
    
    
    
    
    
    
    
    
    